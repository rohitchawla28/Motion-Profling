package org.firstinspires.ftc.teamcode.PathGeneration;

public class Point {
    private double x;
    private double y;

    public Point(double xCoord, double yCoord) {
        this.x = xCoord;
        this.y = yCoord;
    }

    public double getX() {
        return x;
    }

    public double getY() {
        return y;
    }
}
